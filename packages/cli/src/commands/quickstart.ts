export async function runQuickstart(args: ParsedArgs): Promise<number> {
  const out = new Output({ json: flagAsBool(args, "json") });
  const cwd = process.cwd();
  const apiPort = flagAsInt(args, "api-port") ?? 8100;
  const adminPort = flagAsInt(args, "admin-port") ?? 8080;
  const includeAdmin = !flagAsBool(args, "no-admin");
  const doSeed = !flagAsBool(args, "no-seed");
  const baseUrl = flagAsString(args, "statewave-url") ?? `http://localhost:${apiPort}`;
  // Subject comes from real git identity (remote → repo:owner/name, else the
  // work-tree basename), NOT the cwd name. Undefined when this isn't a repo and
  // no explicit --subject was given — we then skip seeding rather than invent a
  // bogus subject like `repo:<home-dir>`.
  const identity = resolveRepoIdentity(cwd);
  const subject = flagAsString(args, "subject") ?? identity?.subject;
  // Optional-stage warnings; the final summary and exit reflect them honestly.
  const warnings: string[] = [];

  if (flagAsBool(args, "down")) {
    if (!existsSync(COMPOSE_PATH)) {
      out.log("No quickstart stack found — nothing to stop.");
      return 0;
    }
    const downArgs = ["compose", "-p", PROJECT_NAME, "-f", COMPOSE_PATH, "down"];
    if (flagAsBool(args, "purge")) downArgs.push("--volumes");
    try {
      execFileSync("docker", downArgs, { stdio: "inherit" });
      out.log("Quickstart stack stopped.");
      return 0;
    } catch (err) {
      out.error(`docker compose down failed: ${(err as Error).message}`);
      return 1;
    }
  }

  // Welcome banner — the first thing a normal run shows (skipped in --json so
  // machine output stays clean). Sets context for the prompts that follow.
  if (!out.isJson()) {
    out.log("");
    out.log(`${bold("Welcome to Statewave")} ${dim("— a local memory layer for your AI tools.")}`);
    out.log(dim("One command: start a server, wire your MCP clients, and seed your repos — all on your machine."));
  }

  // Environment preflight (informational): Node already runs this code; Git is
  // needed for repo identity + seeding. Docker is checked lazily, only if we
  // actually need to start a server.
  if (!nodeMeetsMinimum(process.version)) {
    out.warn(`Node ${process.version} is below the supported minimum (>= ${MIN_NODE_MAJOR}); some steps may misbehave.`);
  }
  if (!gitAvailable()) {
    out.warn("git not found on PATH — repository detection and seeding will be skipped.");
  }

  // Docker preflight — surface a missing install before any interactive prompts
  // so the user doesn't answer several questions only to hit a hard stop.
  // Skip when reusing an external server (--statewave-url).
  let serverAlreadyUp = false;
  if (!flagAsString(args, "statewave-url")) {
    serverAlreadyUp = await checkHealth(baseUrl);
    if (serverAlreadyUp) {
      if (!out.isJson()) out.log(`\n${green("✓")} Statewave server already running at ${baseUrl} — reusing it.`);
    } else if (!(await ensureDockerReady(out))) return 1;
  }

  // Which clients to configure: explicit --client a,b,c | --all | interactive pick | auto-detect.
  let clients: ClientDef[];
  {
    const explicit = flagAsString(args, "client");
    if (explicit) {
      const resolved: ClientDef[] = [];
      for (const id of explicit.split(",").map((s) => s.trim()).filter(Boolean)) {
        const c = findClient(id);
        if (!c) {
          out.error(`unknown client: ${id}`, `supported: ${CLIENTS.map((x) => x.id).join(", ")}`);
          return 2;
        }
        resolved.push(c);
      }
      clients = resolved;
    } else if (flagAsBool(args, "all")) {
      clients = CLIENTS.slice();
    } else {
      const detectedIds = new Set(detectClients());
      const detected = CLIENTS.filter((c) => detectedIds.has(c.id));
      const interactive = !!process.stdin.isTTY && !out.isJson();
      const assumeYes = flagAsBool(args, "yes") || flagAsBool(args, "y");
      if (interactive && !assumeYes) {
        clients = await promptClientSelection(out, detectedIds);
      } else if (detected.length) {
        clients = detected;
        out.log(dim(`Configuring detected clients: ${detected.map((c) => c.id).join(", ")} (--client to narrow).`));
      } else {
        clients = [findClient("claude-desktop")!];
        out.log(dim("No MCP clients detected — defaulting to Claude Desktop (use --client/--all)."));
      }
    }
  }
  if (clients.length === 0) {
    out.log(dim("No clients selected — starting/seeding the server but configuring nothing."));
  }

  // Memory engine: offline heuristic by default, or an LLM provider via LiteLLM.
  // Provider config only matters when WE start the server (a reused server is
  // already configured). Resolve flag/env config up front to warn on reuse.
  const forceOffline = flagAsBool(args, "no-llm");
  const flagKey = forceOffline
    ? undefined
    : flagAsString(args, "llm-api-key") ??
      process.env.STATEWAVE_LITELLM_API_KEY ??
      process.env.OPENAI_API_KEY;
  const flagProvider = forceOffline ? undefined : flagAsString(args, "provider");
  let providerConfig: ProviderConfig | undefined =
    !forceOffline && (flagProvider || flagKey)
      ? {
          provider: flagProvider ?? "openai",
          model: flagAsString(args, "llm-model"),
          apiKey: flagKey,
          apiBase: flagAsString(args, "api-base"),
          embeddingModel: flagAsString(args, "embedding-model"),
        }
      : undefined;

  // 1. Server — reuse an existing healthy one, otherwise bring up the stack.
  //    Docker was already verified ready in the preflight above (when managing
  //    our own stack), so we don't call ensureDockerReady again here.
  let startedStack = false;
  if (serverAlreadyUp || await checkHealth(baseUrl)) {
    // serverAlreadyUp case: already announced before the client prompt.
    // --statewave-url case: announce here (first time we know the server is reachable).
    if (!serverAlreadyUp) {
      out.log(`${green("✓")} Statewave server reachable at ${baseUrl} — reusing it.`);
    }
    if (providerConfig) {
      out.warn(
        "an LLM provider was configured, but quickstart is reusing an already-running server — " +
          "it has no effect here; configure the provider on that server instead",
      );
    }
  } else {
    // Choose the memory engine before starting (interactive when none was given).
    if (!providerConfig && !forceOffline && !flagAsBool(args, "no-llm-prompt") && !out.isJson()) {
      providerConfig = (await promptMemoryEngine(out)) ?? undefined;
    }
    const llmEnvVars = providerConfig ? buildProviderEnv(providerConfig) : {};
    const memoryLabel = providerConfig
      ? `LLM compiler via ${providerConfig.provider}${
          llmEnvVars.STATEWAVE_EMBEDDING_PROVIDER === "litellm"
            ? " + semantic embeddings"
            : " + keyword retrieval (provider has no embeddings)"
        }`
      : "offline heuristic compiler + keyword retrieval (no key, no cost)";
    out.log("");
    out.log(bold(`Starting Statewave (api${includeAdmin ? " + admin" : ""} + db) via docker compose…`));
    out.log(`  memory: ${memoryLabel}`);
    await mkdir(STATE_DIR, { recursive: true });
    await writeFile(COMPOSE_PATH, renderComposeFile({ apiPort, adminPort, includeAdmin }), "utf8");
    try {
      execFileSync("docker", ["compose", "-p", PROJECT_NAME, "-f", COMPOSE_PATH, "up", "-d", "--pull", "always"], {
        stdio: "inherit",
        // Pass the provider env to compose; the compose file interpolates these
        // so the API key never lands on disk.
        env: { ...process.env, ...llmEnvVars },
      });
    } catch (err) {
      out.error(`docker compose up failed: ${(err as Error).message}`);
      return 1;
    }
    const healthy = await withSpinner(
      "Waiting for the API to become healthy…",
      () => waitForHealth(baseUrl, { timeoutMs: 180_000, intervalMs: 2_000 }),
      { active: !out.isJson() },
    );
    if (!healthy) {
      out.error(
        `server did not become healthy at ${baseUrl} within 3 minutes`,
        `inspect logs: docker compose -p ${PROJECT_NAME} -f ${COMPOSE_PATH} logs api`,
      );
      return 1;
    }
    startedStack = true;
    out.log(`${green("✓")} Server healthy at ${baseUrl}.`);
  }

  // Select the repositories to set up (config + seed) BEFORE configuring clients,
  // so project-scoped clients are configured INSIDE each selected repository.
  const reposToSeed = doSeed ? await selectRepos(out, args, identity, flagAsString(args, "subject")) : [];

  // 2. Configure clients. Project-scoped clients (Claude Code, Cursor, VS Code)
  //    — and any client with a repo instruction file (Codex) — are written into
  //    each selected repository so opening any of them targets that repo's
  //    subject. Pure user-scoped chat apps (Claude Desktop) are configured once.
  const serverBin = resolveLocalServerBin();
  const spec = buildServerSpec({ statewaveUrl: baseUrl, serverBin });
  let pasteBlock: string | undefined;
  out.log("");
  const configTargets: Array<{ root: string; subject: string }> = reposToSeed.length
    ? reposToSeed.map((r) => ({ root: r.root, subject: r.subject }))
    : [{ root: identity?.root ?? cwd, subject: subject ?? "repo:workspace" }];
  const noSubjectAnywhere = reposToSeed.length === 0 && !subject;
  const newlyConfigured: ClientDef[] = [];
  const alreadyConfigured: ClientDef[] = [];
  for (const client of clients) {
    try {
      const perRepo = client.scope === "project" || Boolean(client.instructionFile);
      const targets = perRepo ? configTargets : configTargets.slice(0, 1);
      let lastPaste: string | undefined;
      let anyChanged = false;
      for (const t of targets) {
        const r = await writeInit(client, spec, t.subject, t.root, {
          skipInstructions: flagAsBool(args, "no-instructions") || noSubjectAnywhere,
        });
        if (r.pasteBlock) lastPaste = r.pasteBlock;
        if (r.applied.some((a) => a.action !== "skip")) anyChanged = true;
      }
      if (lastPaste) pasteBlock = lastPaste;
      if (anyChanged) {
        newlyConfigured.push(client);
        out.log(
          `${green("✓")} Configured ${bold(client.label)}${targets.length > 1 ? gray(` (${targets.length} repos)`) : ""}`,
        );
      } else {
        alreadyConfigured.push(client);
        out.log(`${dim("–")} ${bold(client.label)} already configured`);
      }
    } catch (err) {
      out.error(`failed to configure ${client.label}: ${(err as Error).message}`);
      return 1;
    }
  }

  // 2b. Install the IDE Companion (capture side) for any chosen editor. Picking
  //     VS Code / Cursor is the consent — no second prompt. Opt out with
  //     --no-install-extension. Failures are soft (don't fail the whole run).
  if (!flagAsBool(args, "no-install-extension")) {
    const editors = clients.filter((c) => isEditorClient(c.id));
    if (editors.length) out.log("");
    const vsix = flagAsString(args, "extension-vsix");
    const target = vsix ?? EXTENSION_ID;
    const handled = new Set<string>(); // resolved binaries — forks sharing a CLI are handled once
    for (const e of editors) {
      const r = installAndVerify(e.id, target, { vsixGiven: Boolean(vsix) });
      const via =
        r.via && !e.label.includes(r.via) && !r.via.includes(e.label) ? `${e.label} (${r.via})` : e.label;
      if (r.binary && handled.has(r.binary)) {
        out.log(dim(`  ${e.label}: same editor as a prior selection (${r.via}) — already handled.`));
        continue;
      }
      if (r.binary) handled.add(r.binary);
      switch (r.status) {
        case "no-cli":
          warnings.push(`${e.label}: editor CLI not found — IDE Companion not installed`);
          out.log(`  ${yellow("!")} ${e.label}: CLI not found — install "${EXTENSION_ID}" from its Extensions panel.`);
          break;
        case "already":
          out.log(`${green("✓")} IDE Companion already installed in ${bold(via)}`);
          break;
        case "installed":
        case "updated":
          out.log(`${green("✓")} IDE Companion ${r.status} and verified in ${bold(via)}`);
          break;
        case "failed":
          warnings.push(`IDE Companion install failed in ${via}`);
          out.log(`  ${red("✗")} IDE Companion install failed in ${bold(via)}: ${r.message}`);
          out.log(dim(`    Install manually: open ${e.label} → Extensions → search "${EXTENSION_ID}".`));
          break;
        case "unverified":
          warnings.push(`IDE Companion install in ${via} could not be verified`);
          out.log(`  ${yellow("!")} IDE Companion install reported success in ${bold(via)} but couldn't be verified — open its Extensions panel to confirm.`);
          break;
      }
    }
  }

  // 3. Repository selection + seeding (zero, one, or many). Counts are verified
  //    against the server, never trusted from the seed command's own output.
  interface SeedResult { subject: string; root: string; ok: boolean; episodes?: number }
  const seedResults: SeedResult[] = [];
  if (doSeed && reposToSeed.length === 0) {
    out.log("");
    out.log(dim("No repository selected — seeding skipped. Seed later: statewave-connectors mcp seed --write"));
  }
  for (const repo of reposToSeed) {
    out.log("");
    out.log(bold(`Seeding ${repo.subject}`) + dim(`  (${repo.root})`));
    const seedCode = await runMcpSeed({
      positional: ["mcp", "seed"],
      flags: { subject: repo.subject, write: true, "statewave-url": baseUrl, "repo-path": repo.root, quiet: true },
    });
    const counts = await subjectCounts(baseUrl, repo.subject);
    const ok = seedCode === 0 && !!counts && counts.episodes > 0;
    seedResults.push({ subject: repo.subject, root: repo.root, ok, episodes: counts?.episodes });
    // Rendered per-subject in the summary below; tracked as a hard failure (not a
    // soft warning) because seeding is an action the user explicitly asked for.
  }

  // 4. Honest summary. Two severities, kept distinct: a FAILURE is a requested
  //    action that didn't complete (a selected repo didn't seed) and drives a
  //    non-zero exit; a WARNING is advisory (the optional IDE Companion) and
  //    does not. The headline and exit code never overstate what happened.
  const seededOk = seedResults.filter((r) => r.ok);
  const outcome = quickstartOutcome(seedResults, warnings.length);
  out.log("");
  if (outcome.severity === "error") {
    out.log(
      bold(
        `Statewave quickstart completed with errors — ${outcome.failedSeeds} of ${seedResults.length} ` +
          `${seedResults.length === 1 ? "repository" : "repositories"} did not seed.`,
      ),
    );
  } else if (outcome.severity === "warning") {
    out.log(bold(`Statewave quickstart complete — ${warnings.length} warning${warnings.length === 1 ? "" : "s"}.`));
  } else {
    out.log(bold("Statewave quickstart complete."));
  }
  out.log(`  ${green("✓")} Server: ${baseUrl}`);
  if (includeAdmin) {
    out.log(`  ${green("✓")} Admin dashboard: ${cyan(`http://localhost:${adminPort}`)}`);
  }
  if (newlyConfigured.length) {
    out.log(`  ${green("✓")} Configured: ${newlyConfigured.map((c) => c.label).join(", ")}`);
  }
  if (alreadyConfigured.length) {
    out.log(`  ${dim("–")} Already configured: ${alreadyConfigured.map((c) => c.label).join(", ")}`);
  }
  if (doSeed && reposToSeed.length === 0) out.log(`  ${dim("–")} Seeding skipped (no repository selected)`);
  for (const r of seedResults) {
    out.log(
      r.ok
        ? `  ${green("✓")} ${r.subject} — ${r.episodes} episodes (verified)`
        : `  ${red("✗")} ${r.subject} — seeding failed (re-run: statewave-connectors mcp seed --subject ${r.subject} --repo-path ${r.root} --write)`,
    );
  }
  if (seededOk.length > 0 && includeAdmin) {
    out.log(`  ${dim("→")} Inspect seeded memory: ${cyan(`http://localhost:${adminPort}`)}`);
  }
  for (const w of warnings) out.log(`  ${yellow("!")} ${w}`);

  // Per-client: how to start using Statewave right now.
  const testRepo = seededOk[0] ?? configTargets[0];
  if (clients.length && testRepo) {
    out.log("");
    out.log(bold("Using Statewave"));
    out.log(dim("Your AI tools now have persistent memory of this codebase."));
    out.log(dim("Ask why code is the way it is, not just what it does."));
    for (const c of clients) {
      out.log("");
      const needsPaste = c.id === "claude-desktop" && pasteBlock;
      out.log(`  ${bold(c.label)}${needsPaste ? `  ${dim("← one-time paste needed")}` : ""}`);
      for (const line of clientUsageBlock(c, testRepo.root, testRepo.subject, needsPaste ? pasteBlock : undefined)) {
        out.log(`    ${line}`);
      }
    }
  }

  // Always save the Claude Desktop paste block to file as a fallback reference.
  if (pasteBlock) {
    const instrPath = resolve(STATE_DIR, "chat-instructions.md");
    try {
      await mkdir(STATE_DIR, { recursive: true });
      await writeFile(instrPath, pasteBlock.trimEnd() + "\n", "utf8");
    } catch {
      // non-fatal
    }
  }
  if (startedStack) {
    out.log("");
    out.log(dim("Stop the stack later with: statewave-connectors quickstart --down"));
  }
  // Partial success stays partial: a non-zero exit if any requested seed failed,
  // so scripts and CI can tell a clean run from one that needs a re-seed. Soft
  // warnings (optional IDE Companion) do not change the exit code.
  return outcome.exitCode;
}
