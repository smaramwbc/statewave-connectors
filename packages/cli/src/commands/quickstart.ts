type Parsed<T> = { ok: true; value: T } | { ok: false; message: string };
const ok = <T,>(value: T): Parsed<T> => ({ ok: true, value });
const bad = (message: string): Parsed<never> => ({ ok: false, message });

/**
 * Render a prompt input line legibly on BOTH light and dark terminals. The fix
 * for "the hints are washed out": never `dim` text the user must read to answer.
 * A cyan marker (a mid-tone that stays visible on either background) anchors the
 * eye; the hint itself is the terminal's default foreground, which the terminal
 * already contrasts correctly against its own theme. We don't try to detect the
 * background — there's no reliable cross-terminal way — so theme-agnostic styling
 * (default fg + a safe accent, never dim, never a hardcoded light/dark colour) is
 * the robust choice.
 */
function promptText(hint: string): string {
  return `  ${cyan("›")} ${hint}: `;
}

/** A section question header: a cyan `?` marker + bold text — colourful and
 *  legible on light and dark terminals (bold uses the theme's own foreground). */
function questionHeader(text: string): string {
  return `${cyan("?")} ${bold(text)}`;
}

/**
 * Ask until `parse` accepts the answer. On anything invalid it re-prompts with a
 * hint instead of silently proceeding to a fallback — a mistyped choice should
 * never quietly pick the wrong thing. Ctrl-C still aborts the whole run.
 */
export async function askValid<T>(
  out: Output,
  ask: (q: string) => Promise<string>,
  question: string,
  parse: (answer: string) => Parsed<T>,
): Promise<T> {
  for (;;) {
    const r = parse(await ask(question));
    if (r.ok) return r.value;
    out.log(`  ${yellow("!")} ${r.message}`);
  }
}

/** Required API-key prompt: re-asks when blank, with an explicit 'offline' escape. */
async function askApiKey(out: Output, ask: (q: string) => Promise<string>): Promise<string | undefined> {
  const key = await askValid<string | undefined>(
    out,
    ask,
    promptText("API key (never written to config or printed; type 'offline' to cancel)"),
    (a) => {
      const t = a.trim();
      if (t.toLowerCase() === "offline") return ok<string | undefined>(undefined);
      if (t) return ok<string | undefined>(t);
      return bad("enter the API key, or type 'offline' to use local/offline instead.");
    },
  );
  if (!key) out.warn("using local/offline.");
  return key;
}

/**
 * Interactive memory-engine selection: offline (heuristic + keyword) or an LLM
 * provider via LiteLLM. Returns a ProviderConfig, or null for offline. Only runs
 * on a TTY; the API key is read inline but never echoed back in any summary.
 * Every constrained prompt re-asks on invalid input rather than falling through.
 */
async function promptMemoryEngine(out: Output): Promise<ProviderConfig | null> {
  if (!process.stdin.isTTY) return null;
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  const ask = (q: string): Promise<string> => new Promise((res) => rl.question(q, res));
  try {
    out.log("");
    out.log(questionHeader("How should Statewave build and search memory?"));
    out.log(`  ${cyan("1")}. ${bold("Configure an LLM provider")} ${green("(recommended)")} — cleaner fact extraction + semantic retrieval (LiteLLM).`);
    out.log(`  ${cyan("2")}. Local / offline — built-in heuristic compiler + keyword retrieval. No key, no cost.`);
    const mode = await askValid(out, ask, promptText("1 = LLM provider (recommended) · 2 = local/offline · Enter = 1"), (a) => {
      const t = a.trim();
      if (t === "" || t === "1") return ok("llm" as const);
      if (t === "2") return ok("offline" as const);
      return bad("enter 1, 2, or just press Enter.");
    });
    if (mode === "offline") return null;

    out.log("");
    out.log(questionHeader("Choose a provider") + dim("  (via LiteLLM — not just OpenAI)"));
    PROVIDERS.forEach((p, i) => out.log(`  ${cyan(String(i + 1))}. ${p.label}`));
    const provider = await askValid(out, ask, promptText(`number 1–${PROVIDERS.length}`), (a) => {
      const n = Number.parseInt(a.trim(), 10);
      if (Number.isInteger(n) && n >= 1 && n <= PROVIDERS.length) return ok(PROVIDERS[n - 1]!);
      return bad(`enter a number from 1 to ${PROVIDERS.length}.`);
    });

    // Freeform LiteLLM id — no live list to offer, so just take a model + key.
    if (provider.id === "custom") {
      const model = await askValid(out, ask, promptText("Model (LiteLLM id, e.g. anthropic/claude-3-5-haiku-latest)"), (a) =>
        a.trim() ? ok(a.trim()) : bad("enter a LiteLLM model id."),
      );
      const apiKey = await askApiKey(out, ask);
      if (!apiKey) return null;
      return { provider: provider.id, model, apiKey };
    }

    // Credentials FIRST — needed to ask the provider what it currently serves.
    let apiBase: string | undefined;
    if (provider.needsApiBase) {
      const baseHint = provider.defaultApiBase ? `[Enter = ${provider.defaultApiBase}]` : "(required)";
      apiBase = await askValid(out, ask, promptText(`API base URL ${baseHint}`), (a) => {
        const t = a.trim() || provider.defaultApiBase;
        return t ? ok(t) : bad("an API base URL is required for this provider.");
      });
    }
    // Credentials + live model discovery in one step: the model fetch doubles as
    // a key check. A rejected key (wrong / expired) re-asks rather than wiring up
    // a server that can't authenticate; a network failure can't verify the key,
    // so we proceed with a warning and the built-in default. Listing live also
    // means a deprecated model is never offered as "best" (offline stays
    // first-class via the fallback).
    let apiKey: string | undefined;
    let catalog: ModelCatalog;
    for (;;) {
      if (provider.needsApiKey) {
        apiKey = await askApiKey(out, ask);
        if (!apiKey) return null;
      }
      out.log(dim(`  ${provider.needsApiKey ? "Checking your key and fetching" : "Fetching"} models from ${provider.label}…`));
      catalog = await listProviderModels(provider, { apiKey, apiBase });
      if (catalog.reason === "auth" && provider.needsApiKey) {
        out.warn(`${provider.label} rejected that key (${catalog.note ?? "unauthorized"}). It may be wrong or expired — try again, or type 'offline'.`);
        continue;
      }
      if (catalog.reason === "network") {
        out.warn(`couldn't verify the key with ${provider.label} (${catalog.note ?? "network error"}); proceeding — the server will use it as given.`);
      }
      break;
    }
    let model: string;
    if (catalog.source === "live" && catalog.models.length > 0 && catalog.recommended) {
      const shown = catalog.models.slice(0, 8);
      out.log(`  ${bold("Latest models")} ${dim("(recommended first)")}`);
      shown.forEach((m, i) =>
        out.log(`    ${cyan(String(i + 1))}. ${m}${m === catalog.recommended ? ` ${green("(recommended)")}` : ""}`),
      );
      model = await askValid(out, ask, promptText(`Enter = ${catalog.recommended}, or a number / model id`), (a) => {
        const t = a.trim();
        if (/^\d+$/.test(t)) {
          const n = Number.parseInt(t, 10);
          if (n < 1 || n > shown.length) return bad(`pick a number from 1 to ${shown.length}, or type a model id.`);
        }
        return ok(resolveModelAnswer(t, shown, catalog.recommended!));
      });
    } else {
      if (catalog.note) out.log(dim(`  ${catalog.note} — using the built-in default.`));
      const usableDefault = provider.defaultModel && !provider.defaultModel.includes("<") ? provider.defaultModel : "";
      model = await askValid(out, ask, promptText(`Model ${usableDefault ? `[Enter = ${usableDefault}]` : "(LiteLLM model id)"}`), (a) => {
        const t = a.trim() || usableDefault;
        return t ? ok(t) : bad("enter a LiteLLM model id.");
      });
    }

    let embeddingModel: string | undefined;
    if (!provider.defaultEmbeddingModel) {
      out.log(dim(`  ${provider.label} has no default embedding model — retrieval uses keywords unless you add one.`));
      embeddingModel = (await ask(promptText("Embedding model [Enter = keyword retrieval]"))).trim() || undefined;
    }

    return { provider: provider.id, model, apiKey, apiBase, embeddingModel };
  } finally {
    rl.close();
  }
}

/**
 * Parse a client-selection answer into the chosen clients. Pure, so it's unit
 * tested directly. Empty (just Enter) selects the detected set (or all when
 * nothing detected); `a`/`all` selects everything from `list`; `n`/`none`
 * selects nothing; otherwise 1-based indices into `list` (`1,3` / `1 3`).
 * `list` defaults to the full registry for backward compatibility.
 */
export function parseClientSelection(answer: string, detected: ReadonlyArray<ClientDef>, list: ReadonlyArray<ClientDef> = CLIENTS): ClientDef[] {
  const t = answer.trim().toLowerCase();
  if (t === "") return detected.length ? [...detected] : [...list];
  if (t === "a" || t === "all") return [...list];
  if (t === "n" || t === "none") return [];
  const nums = t
    .split(/[\s,]+/)
    .map((x) => Number.parseInt(x, 10))
    .filter((n) => Number.isInteger(n) && n >= 1 && n <= list.length);
  return [...new Set(nums)].map((n) => list[n - 1]!);
}

/** Interactively show only the detected clients and let the user pick a subset. */
async function promptClientSelection(out: Output, detectedIds: Set<string>): Promise<ClientDef[]> {
  const detected = CLIENTS.filter((c) => detectedIds.has(c.id));
  // If nothing is detected, fall back to showing the full registry so the user
  // can still pick manually.
  const list = detected.length ? detected : CLIENTS;
  out.log("");
  out.log(questionHeader("Which MCP clients should I set up?"));
  list.forEach((c, i) => {
    const suffix = list === CLIENTS ? ` ${gray("not detected")}` : "";
    out.log(`  ${cyan(String(i + 1))}. ${c.label}${suffix}`);
  });
  const hint = `Enter = ${list === CLIENTS ? "all" : "detected"}, e.g. 1,2 = pick, 'n' = none`;
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  const ask = (q: string): Promise<string> => new Promise((res) => rl.question(q, res));
  try {
    return await askValid(out, ask, promptText(hint), (answer) => {
      const t = answer.trim().toLowerCase();
      const chosen = parseClientSelection(answer, list, list);
      if (chosen.length === 0 && t !== "" && t !== "n" && t !== "none") {
        return bad(`didn't recognize "${answer.trim()}" — enter Enter, n, or numbers like 1,2.`);
      }
      return ok(chosen);
    });
  } finally {
    rl.close();
  }
}

// The Statewave IDE Companion — the *capture* half: it auto-ingests your file
// and git activity into Statewave as you work. Complements the MCP config,
// which only lets the assistant *read* memory. Cross-platform resolution +
// verified install live in ./extensions.ts (EXTENSION_ID, installAndVerify,
// isEditorClient), unit-tested across the full 8-case matrix.

export type QuickstartSeverity = "ok" | "warning" | "error";

/**
 * The run's overall outcome, kept as one small honest rule so the headline and
 * exit code can never disagree. A FAILURE is a requested action that did not
 * complete (a selected repo did not seed) → severity "error", exit 1. A WARNING
 * is advisory (the optional IDE Companion) → severity "warning", exit 0. Server
 * and client-config failures exit earlier and never reach here.
 */
export function quickstartOutcome(
  seedResults: ReadonlyArray<{ ok: boolean }>,
  warningCount: number,
): { severity: QuickstartSeverity; exitCode: 0 | 1; failedSeeds: number } {
  const failedSeeds = seedResults.filter((r) => !r.ok).length;
  if (failedSeeds > 0) return { severity: "error", exitCode: 1, failedSeeds };
  if (warningCount > 0) return { severity: "warning", exitCode: 0, failedSeeds: 0 };
  return { severity: "ok", exitCode: 0, failedSeeds: 0 };
}

/**
 * Verified episode count for a subject, read from `/v1/timeline` — which reads
 * the episodes table directly. We deliberately do NOT use `/v1/subjects`: it's a
 * lagging aggregate that can report 0 right after a write (confirmed against the
 * live server), so trusting it would produce false "seeding failed" results.
 * Returns null on any error. Memory count is intentionally omitted — compilation
 * is asynchronous, so it isn't reliably non-zero immediately after seeding.
 */
export async function subjectCounts(
  baseUrl: string,
  subject: string,
  fetchImpl: typeof fetch = fetch,
): Promise<{ episodes: number } | null> {
  try {
    const url = `${baseUrl.replace(/\/+$/, "")}/v1/timeline?subject_id=${encodeURIComponent(subject)}&limit=1000`;
    const res = await fetchImpl(url);
    if (!res.ok) return null;
    const data = (await res.json()) as { episodes?: unknown[] };
    return Array.isArray(data.episodes) ? { episodes: data.episodes.length } : null;
  } catch {
    return null;
  }
}

/** Parse a 1-based multi-select over `count` discovered repos. Pure (tested). */
export function parseRepoSelection(answer: string, count: number): number[] {
  const t = answer.trim().toLowerCase();
  if (t === "" || t === "a" || t === "all") return Array.from({ length: count }, (_, i) => i);
  if (t === "n" || t === "none") return [];
  const nums = t
    .split(/[\s,]+/)
    .map((x) => Number.parseInt(x, 10))
    .filter((n) => Number.isInteger(n) && n >= 1 && n <= count);
  return [...new Set(nums)].map((n) => n - 1);
}

function showRepoList(out: Output, repos: RepoIdentity[]): void {
  repos.forEach((r, i) => {
    out.log(`  ${cyan(String(i + 1))}. ${bold(r.subject)}${gray(`  ${r.root}`)}${r.fromRemote ? "" : dim("  (no remote)")}`);
  });
}

async function ask(rl: ReturnType<typeof createInterface>, q: string): Promise<string> {
  return (await new Promise<string>((res) => rl.question(q, res))).trim();
}

/** Render discovered repos and parse a multi-select. Assumes repos.length > 0. */
async function pickFromRepos(
  out: Output,
  rl: ReturnType<typeof createInterface>,
  repos: RepoIdentity[],
  truncated: boolean,
  root: string,
): Promise<RepoIdentity[]> {
  out.log("");
  out.log(`Found ${repos.length}${truncated ? "+ (truncated)" : ""} repositories under ${root}:`);
  showRepoList(out, repos);
  const sel = parseRepoSelection(
    await ask(rl, promptText("Enter/a = all, e.g. 1,3 = pick, n = none")),
    repos.length,
  );
  return sel.map((i) => repos[i]!);
}

/** Discover under a root, then let the user multi-select (used by --discover). */
async function discoverAndPick(out: Output, root: string): Promise<RepoIdentity[]> {
  const resolved = resolve(root);
  const { repos, truncated } = discoverRepos(resolved);
  if (repos.length === 0) {
    out.warn(`No git repositories found under ${resolved} — nothing to seed.`);
    return [];
  }
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  try {
    return await pickFromRepos(out, rl, repos, truncated, resolved);
  } finally {
    rl.close();
  }
}

/** Expand a leading `~` and resolve to an absolute path. */
export function expandUserPath(p: string): string {
  return resolve(p.replace(/^~(?=$|\/)/, homedir()));
}

/** A typed answer that looks like a filesystem path (absolute, ~, ./, or C:\). */
export function looksLikePath(s: string): boolean {
  return /^[~./]/.test(s) || /^[A-Za-z]:[\\/]/.test(s);
}

/** A typed answer that looks like a remote repository URL (https/http/git/ssh). */
export function looksLikeUrl(s: string): boolean {
  return /^https?:\/\/|^git@|^git:\/\/|^ssh:\/\//.test(s);
}

/** Interactive repository selection: current repo, discover, path, or skip. */
async function promptRepoSelection(out: Output, identity: RepoIdentity | undefined): Promise<RepoIdentity[]> {
  out.log("");
  out.log(questionHeader("Which repositories should Statewave seed?"));
  let n = 0;
  const opts: Array<"current" | "discover" | "path" | "skip"> = [];
  if (identity) {
    out.log(`  ${cyan(String(++n))}. Current repository  ${gray(identity.root)}  → ${bold(identity.subject)}`);
    opts.push("current");
  }
  out.log(`  ${cyan(String(++n))}. Discover repositories under a folder`);
  opts.push("discover");
  out.log(`  ${cyan(String(++n))}. Enter a local repository path`);
  opts.push("path");
  out.log(`  ${cyan(String(++n))}. Skip seeding`);
  opts.push("skip");

  const rl = createInterface({ input: process.stdin, output: process.stdout });
  const def = identity ? "current" : "discover";
  try {
    // Loop until the user picks repos or EXPLICITLY skips — never silently skip
    // on unrecognized input. A path typed here (the natural thing to do at this
    // prompt) is DWIM'd: a git repo → use it; a folder → discover under it.
    for (;;) {
      const raw = await ask(rl, promptText(`Enter = ${def === "current" ? "current repo" : "discover"}, a number, or a path`));

      if (raw && looksLikeUrl(raw)) {
        out.log(`  ${yellow("!")} Statewave seeds from a local clone, not a remote URL.`);
        out.log(`  ${dim("Clone it first:")}  git clone ${raw} ~/your-path`);
        out.log(`  ${dim("Then enter that local path here, or press Enter to use the current repo.")}`);
        continue;
      }

      if (raw && looksLikePath(raw)) {
        const target = expandUserPath(raw);
        const repo = resolveRepoIdentity(target);
        if (repo) return [repo];
        const { repos, truncated } = discoverRepos(target);
        if (repos.length > 0) {
          const picked = await pickFromRepos(out, rl, repos, truncated, target);
          if (picked.length) return picked;
          return []; // found repos, user picked none → an explicit skip
        }
        out.log(
          `  ${yellow("!")} ${existsSync(target) ? `no git repositories under ${target}` : `${target} doesn't exist`}` +
            ` — enter a different path, a number, or 'n' to skip.`,
        );
        continue;
      }

      const lc = raw.toLowerCase();
      const choice: "current" | "discover" | "path" | "skip" | undefined =
        raw === ""
          ? def
          : lc === "n" || lc === "none" || lc === "skip"
            ? "skip"
            : (() => {
                const num = Number.parseInt(raw, 10);
                return Number.isInteger(num) && num >= 1 && num <= opts.length ? opts[num - 1] : undefined;
              })();

      if (!choice) {
        out.log(`  ${yellow("!")} enter a number from 1 to ${opts.length}, a path, or 'n' to skip.`);
        continue;
      }

      switch (choice) {
        case "current":
          return identity ? [identity] : [];
        case "skip":
          return [];
        case "discover": {
          const folder = (await ask(rl, promptText(`Folder to search [Enter = ${process.cwd()}]`))) || process.cwd();
          const root = expandUserPath(folder);
          const { repos, truncated } = discoverRepos(root);
          if (repos.length === 0) {
            out.log(`  ${yellow("!")} no git repositories under ${root} — enter a different folder, a number, or 'n' to skip.`);
            continue;
          }
          const picked = await pickFromRepos(out, rl, repos, truncated, root);
          if (picked.length) return picked;
          return [];
        }
        case "path": {
          const p = await ask(rl, promptText("Local path to repository (e.g. ~/projects/my-app)"));
          if (p && looksLikeUrl(p)) {
            out.log(`  ${yellow("!")} That looks like a URL — Statewave needs a local clone.`);
            out.log(`  ${dim("Run:")}  git clone ${p} ~/your-path  ${dim("then enter that path.")}`);
            continue;
          }
          const id = p ? resolveRepoIdentity(expandUserPath(p)) : undefined;
          if (id) return [id];
          out.log(`  ${yellow("!")} ${p || "(empty)"} is not a git repository — enter a path, a number, or 'n' to skip.`);
          continue;
        }
      }
    }
  } finally {
    rl.close();
  }
}

/** Resolve the repositories to seed from flags or interaction. */
async function selectRepos(
  out: Output,
  args: ParsedArgs,
  identity: RepoIdentity | undefined,
  explicitSubject: string | undefined,
): Promise<RepoIdentity[]> {
  if (explicitSubject) {
    return [{ root: identity?.root ?? process.cwd(), subject: explicitSubject, fromRemote: identity?.fromRemote ?? false }];
  }
  const reposFlag = flagAsString(args, "repos");
  if (reposFlag) {
    return reposFlag
      .split(",")
      .map((s) => s.trim())
      .filter(Boolean)
      .map((p) => resolveRepoIdentity(resolve(p)))
      .filter((r): r is RepoIdentity => Boolean(r));
  }
  const discoverRoot = flagAsString(args, "discover");
  if (discoverRoot) {
    if (process.stdin.isTTY && !out.isJson() && !flagAsBool(args, "yes")) return discoverAndPick(out, discoverRoot);
    return discoverRepos(resolve(discoverRoot)).repos;
  }
  if (process.stdin.isTTY && !out.isJson() && !flagAsBool(args, "yes")) {
    return promptRepoSelection(out, identity);
  }
  return identity ? [identity] : [];
}

/**
 * Client-specific "test it here" steps with the correct reload behavior:
 * CLI clients need a new terminal; editors need a window reload; the chat app
 * needs a full restart and picks the subject in-app.
 */
function clientTestSteps(client: ClientDef, root: string, subject: string): string[] {
  const ask = `Ask: ${cyan(`"What changed recently in this repository?"`)}`;
  switch (client.id) {
    case "claude":
      // Claude Code is the CLI OR the IDE extension. Only tell the user to run
      // `claude` if it's actually on PATH — otherwise point at the extension
      // (which also auto-reads .mcp.json) so we never suggest a missing command.
      return onPath("claude")
        ? [`Open a terminal in ${root} and run \`claude\`.`, ask]
        : [
            `Open ${root} in your IDE and use the Claude Code extension — it auto-reads .mcp.json (reload the window if it was already open).`,
            `Prefer the CLI? Install it with \`npm i -g @anthropic-ai/claude-code\`, then run \`claude\` in ${root}.`,
            ask,
          ];
    case "codex":
      return onPath("codex")
        ? [`Open a terminal in ${root} and run \`codex\`.`, ask]
        : [
            `The \`codex\` CLI isn't on your PATH. Install the Codex CLI, then run \`codex\` in ${root} (its config is already written).`,
            ask,
          ];
    case "cursor":
      return [`Open ${root} in Cursor (reload the window if it was already open), then open Agent chat.`, ask];
    case "vscode":
      return [`Open ${root} in VS Code (reload the window), then open Copilot Chat in Agent mode.`, ask];
    case "claude-desktop":
      return [
        "Quit and reopen Claude Desktop, then start a new chat.",
        `Ask it to list available repository subjects and pick ${bold(subject)}.`,
      ];
    default:
      return [`Open ${client.label} and ask it about subject ${subject}.`];
  }
}

